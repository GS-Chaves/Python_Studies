#Operador Lógico AND (E)
valor_1 = 20
valor_2 = 21
valor_3 = 19

print(valor_1 < valor_2 and valor_1 < valor_3) # true se todas as proposições forem true

#Operador Lógico OR (ou)
valor_1 = 20
valor_2 = 21
valor_3 = 19

print(valor_1 < valor_2 or valor_1 < valor_3) # false se todas as proposições forem false

#Operador Lógico NOT (negação)
valor_1 = 20
valor_2 = 21
valor_3 = 19

print(not valor_1 < valor_2 or valor_1 < valor_3) # inverte o valor lógico da proposição
