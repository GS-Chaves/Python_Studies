#Operador de Igualdade ( == )
valor_1 = 20
valor_2 = 21
print(valor_1 == valor_2)

#Operador de Comparação Maior que ( > )
valor_1 = 20
valor_2 = 21
print(valor_1 > valor_2)

#Operador de Comparação Maior Igual que ( >= )
valor_1 = 20
valor_2 = 21
print(valor_1 >= valor_2)

#Operador de Comparação Menor que ( < )
valor_1 = 20
valor_2 = 21
print(valor_1 < valor_2)

#Operador de Comparação Menor Igual que ( <= )
valor_1 = 20
valor_2 = 21
print(valor_1 <= valor_2)

#Operador de Comparação Diferente de ( != )
valor_1 = 20
valor_2 = 21
print(valor_1 != valor_2)
